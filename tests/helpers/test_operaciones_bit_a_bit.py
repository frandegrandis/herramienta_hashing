import unittest

from helpers.operaciones_bit_a_bit import rotar_izquierda, bit_not


class OperacionesBitABit(unittest.TestCase):
    def test_left_rotate(self):
        self.assertEqual(rotar_izquierda(0b11111111111111111111111111111110, 1), 0b11111111111111111111111111111101)
        self.assertEqual(rotar_izquierda(0b01111111111111111111111111111110, 1), 0b11111111111111111111111111111100)
        self.assertEqual(rotar_izquierda(0b01101111111111111111111111111111, 1), 0b11011111111111111111111111111110)
        self.assertEqual(rotar_izquierda(0b11111111000000001010101011001100, 1), 0b11111110000000010101010110011001)
        self.assertEqual(rotar_izquierda(0b11111111111111111111111111111110, 5), 0b11111111111111111111111111011111)

    def test_bit_not(self):
        self.assertEqual(bit_not(0b11111111111111111111111111111111), 0b00000000000000000000000000000000)
        self.assertEqual(bit_not(0b00011111111111111111111111111111), 0b11100000000000000000000000000000)
        self.assertEqual(bit_not(0b00011111111111111111111111110000), 0b11100000000000000000000000001111)
